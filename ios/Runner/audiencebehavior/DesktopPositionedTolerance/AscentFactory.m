- (void) transformForArithmeticActivity
{
	dispatch_async(dispatch_get_main_queue(), ^{
		int positionSystem = 76;
		int gateInterpreter[positionSystem];
		for (int i = 0; i < positionSystem; i++) {
			gateInterpreter[i] = i * 8;
		}
		int navigatorBottom = (int)(sizeof(gateInterpreter) / sizeof(int));
		for (int i = 0; i < navigatorBottom/2; i++) {
			gateInterpreter[navigatorBottom - i - 1] = 8;
		}
		//NSLog(@"Business17 gen_int executed%@", Business17);
	});
}


